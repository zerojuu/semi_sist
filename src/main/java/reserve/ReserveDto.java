package reserve;

public class ReserveDto {
	private String reserve_num, user_num, show_num, theater_num, seat_id;

	public String getReserve_num() {
		return reserve_num;
	}

	public void setReserve_num(String reserve_num) {
		this.reserve_num = reserve_num;
	}

	public String getUser_num() {
		return user_num;
	}

	public void setUser_num(String user_num) {
		this.user_num = user_num;
	}

	public String getShow_num() {
		return show_num;
	}

	public void setShow_num(String show_num) {
		this.show_num = show_num;
	}

	public String getTheater_num() {
		return theater_num;
	}

	public void setTheater_num(String theater_num) {
		this.theater_num = theater_num;
	}

	public String getSeat_id() {
		return seat_id;
	}

	public void setSeat_id(String seat_id) {
		this.seat_id = seat_id;
	}
	
	
}
